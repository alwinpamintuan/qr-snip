import { mkdir, rm, writeFile } from 'node:fs/promises';
import { dirname, resolve } from 'node:path';
import QRCode from 'qrcode';
import { PNG } from 'pngjs';

const root = resolve('tests/fixtures/qr');
const generated = resolve(root, 'generated');
await rm(generated, { recursive: true, force: true });
await mkdir(generated, { recursive: true });

const versions = [1, 5, 10, 20, 40];
const corrections = ['L', 'M', 'Q', 'H'];
const transforms = ['normal', 'inverted', 'colored', 'rotated-90', 'resampled', 'blurred', 'occluded'];
const positives = [];

for (let seed = 0; seed < 20; seed += 1) {
  const version = versions[seed % versions.length];
  const errorCorrection = corrections[seed % corrections.length];
  const payload = payloadFor(seed, version);
  const baseBuffer = await QRCode.toBuffer(payload, {
    version,
    errorCorrectionLevel: errorCorrection,
    margin: 4,
    // Version 40 has 177 modules; 1110 px gives every module plus the quiet zone an exact 6 px grid.
    width: version >= 40 ? 1110 : version >= 20 ? 800 : version >= 10 ? 480 : 320,
    color: { dark: '#000000ff', light: '#ffffffff' },
  });
  const base = PNG.sync.read(baseBuffer);

  for (const transform of transforms) {
    const id = `positive-v${String(version).padStart(2, '0')}-${String(seed).padStart(2, '0')}-${transform}`;
    const image = applyTransform(base, transform);
    const relativePath = `generated/${id}.png`;
    await writePng(resolve(root, relativePath), image);
    positives.push({
      id,
      path: relativePath,
      expectedPayload: payload,
      version,
      errorCorrection,
      quietZone: 4,
      transform,
      source: 'Deterministically generated by qrcode 1.5.4',
      license: 'CC0-1.0',
    });
  }
}

const compositeSpecs = [
  ['1080p-1x', 1920, 1080, 1],
  ['1440p-1x', 2560, 1440, 1],
  ['4k-1x', 3840, 2160, 1],
  ['1080p-2x', 3840, 2160, 2],
];
for (const [name, width, height, deviceScale] of compositeSpecs) {
  const payload = `https://example.com/screen/${name}`;
  const qr = PNG.sync.read(await QRCode.toBuffer(payload, { errorCorrectionLevel: 'M', margin: 4, width: 320 * deviceScale }));
  const screen = solidPng(width, height, [233, 224, 232, 255]);
  const x = Math.floor((width - qr.width) / 2);
  const y = Math.floor((height - qr.height) / 2);
  blit(qr, screen, x, y);
  const id = `positive-screen-${name}`;
  const relativePath = `generated/${id}.png`;
  await writePng(resolve(root, relativePath), screen);
  positives.push({
    id,
    path: relativePath,
    expectedPayload: payload,
    version: QRCode.create(payload, { errorCorrectionLevel: 'M' }).version,
    errorCorrection: 'M',
    quietZone: 4,
    transform: `composited-${name}`,
    crop: { x, y, width: qr.width, height: qr.height },
    viewport: { width: width / deviceScale, height: height / deviceScale, deviceScale },
    source: 'Deterministically generated by qrcode 1.5.4 and pngjs 7.0.0',
    license: 'CC0-1.0',
  });
}

const negatives = [];
for (let index = 0; index < 30; index += 1) {
  const id = `negative-${String(index).padStart(2, '0')}`;
  const image = createNegative(index, 192, 192);
  const relativePath = `generated/${id}.png`;
  await writePng(resolve(root, relativePath), image);
  negatives.push({
    id,
    path: relativePath,
    expectedPayload: null,
    version: null,
    errorCorrection: null,
    quietZone: 0,
    transform: index % 3 === 0 ? 'deterministic-noise' : index % 3 === 1 ? 'checkerboard' : 'gradient',
    source: 'Deterministically generated non-QR image',
    license: 'CC0-1.0',
  });
}

await writeFile(resolve(root, 'manifest.json'), `${JSON.stringify({ schemaVersion: 1, positives, negatives }, null, 2)}\n`);
console.log(`Generated ${positives.length} positive and ${negatives.length} negative QR fixtures.`);

function payloadFor(seed, version) {
  if (version === 1) return `q${String(seed).padStart(2, '0')}`;
  const variants = [
    `https://example.com/qr/${seed}?v=${version}`,
    `WIFI:T:WPA;S:Guest-${seed};P:local-only;;`,
    `geo:25.0330,121.5654?q=Fixture+${seed}`,
    `BEGIN:VCARD\nVERSION:3.0\nFN:Fixture ${seed}\nEND:VCARD`,
    `Unicode fixture ${seed}: 台北 • café • 🔒`,
  ];
  return variants[seed % variants.length];
}

function applyTransform(source, transform) {
  const image = clonePng(source);
  if (transform === 'normal') return image;
  if (transform === 'inverted') {
    for (let index = 0; index < image.data.length; index += 4) {
      image.data[index] = 255 - image.data[index];
      image.data[index + 1] = 255 - image.data[index + 1];
      image.data[index + 2] = 255 - image.data[index + 2];
    }
    return image;
  }
  if (transform === 'colored') {
    for (let index = 0; index < image.data.length; index += 4) {
      const dark = image.data[index] < 128;
      const color = dark ? [72, 43, 91] : [255, 247, 255];
      image.data[index] = color[0]; image.data[index + 1] = color[1]; image.data[index + 2] = color[2];
    }
    return image;
  }
  if (transform === 'rotated-90') return rotate90(image);
  if (transform === 'resampled') return resizeNearest(resizeNearest(image, Math.max(64, Math.floor(image.width * .7))), image.width);
  if (transform === 'blurred') return blur(image);
  if (transform === 'occluded') {
    const size = Math.max(4, Math.floor(image.width * .045));
    const start = Math.floor(image.width * .72);
    fillRect(image, start, start, size, size, [255, 255, 255, 255]);
    return image;
  }
  throw new Error(`Unknown transform: ${transform}`);
}

function clonePng(source) {
  const target = new PNG({ width: source.width, height: source.height });
  source.data.copy(target.data);
  return target;
}

function rotate90(source) {
  const target = new PNG({ width: source.height, height: source.width });
  for (let y = 0; y < source.height; y += 1) for (let x = 0; x < source.width; x += 1) {
    copyPixel(source, target, x, y, source.height - 1 - y, x);
  }
  return target;
}

function resizeNearest(source, width) {
  const height = Math.max(1, Math.round(source.height * width / source.width));
  const target = new PNG({ width, height });
  for (let y = 0; y < height; y += 1) for (let x = 0; x < width; x += 1) {
    copyPixel(source, target, Math.min(source.width - 1, Math.floor(x * source.width / width)), Math.min(source.height - 1, Math.floor(y * source.height / height)), x, y);
  }
  return target;
}

function blur(source) {
  const target = clonePng(source);
  for (let y = 1; y < source.height - 1; y += 1) for (let x = 1; x < source.width - 1; x += 1) {
    for (let channel = 0; channel < 3; channel += 1) {
      let sum = 0;
      for (let dy = -1; dy <= 1; dy += 1) for (let dx = -1; dx <= 1; dx += 1) sum += source.data[((y + dy) * source.width + x + dx) * 4 + channel];
      target.data[(y * source.width + x) * 4 + channel] = Math.round(sum / 9);
    }
  }
  return target;
}

function solidPng(width, height, color) {
  const image = new PNG({ width, height });
  for (let index = 0; index < image.data.length; index += 4) {
    image.data[index] = color[0]; image.data[index + 1] = color[1]; image.data[index + 2] = color[2]; image.data[index + 3] = color[3];
  }
  return image;
}

function createNegative(seed, width, height) {
  const image = new PNG({ width, height });
  let state = seed + 1;
  for (let y = 0; y < height; y += 1) for (let x = 0; x < width; x += 1) {
    state = (state * 1664525 + 1013904223) >>> 0;
    const value = seed % 3 === 0 ? (state >>> 24) : seed % 3 === 1 ? ((Math.floor(x / (5 + seed % 7)) + Math.floor(y / (7 + seed % 5))) % 2) * 255 : Math.floor((x + y) / (width + height) * 255);
    const offset = (y * width + x) * 4;
    image.data[offset] = value; image.data[offset + 1] = value; image.data[offset + 2] = value; image.data[offset + 3] = 255;
  }
  return image;
}

function fillRect(image, x, y, width, height, color) {
  for (let py = y; py < Math.min(image.height, y + height); py += 1) for (let px = x; px < Math.min(image.width, x + width); px += 1) {
    const offset = (py * image.width + px) * 4;
    for (let channel = 0; channel < 4; channel += 1) image.data[offset + channel] = color[channel];
  }
}

function blit(source, target, x, y) {
  for (let sy = 0; sy < source.height; sy += 1) for (let sx = 0; sx < source.width; sx += 1) copyPixel(source, target, sx, sy, x + sx, y + sy);
}

function copyPixel(source, target, sx, sy, tx, ty) {
  const sourceOffset = (sy * source.width + sx) * 4;
  const targetOffset = (ty * target.width + tx) * 4;
  for (let channel = 0; channel < 4; channel += 1) target.data[targetOffset + channel] = source.data[sourceOffset + channel];
}

async function writePng(path, image) {
  await mkdir(dirname(path), { recursive: true });
  await writeFile(path, PNG.sync.write(image, { colorType: 6 }));
}
